    public Results(String results,String id)
    {
        try {
            initComponents();
            Connect();
            
            pst=con.prepareStatement("select * from patient where patientno=?");
            pst.setString(1,id);
            rs=pst.executeQuery();
            if(rs.next())
            {
                pno.setText(id);
                String name= rs.getString("name");
                pname.setText(name);
                if(results.equals("low"))
                {
                    result.setText("LOW RISK");
                    result.setBackground(Color.MAGENTA);
                    reco.setText("You have indicated a LOW RISK indicating lower chances of contracting lung cancer.\n However,a number of factors may increase your risk of lung cancer.\n Hence avoid risk factors that can be controlled such as quitting smoking. \nDue to indication of low risk, you are adviced to be diagnosed after sometime.");
                }
                else if(results.equals("medium"))
                {
                    result.setText("MEDIUM RISK");
                    result.setBackground(Color.yellow);
                    reco.setText("You have indicated a MEDIUM RISK indicating moderate chances of contracting lung cancer. \nA number of factors may increase your risk of lung cancer.\n Thus avoid risk factors that can be controlled such as quitting smoking. \nDue to indication of medium risk, you are adviced to be join a program to reduce risk.");
                           
                }
                else if(results.equals("high"))
                {
                    result.setText("HIGH RISK");
                           result.setBackground(Color.red);
                           reco.setText("You have indicated a HIGH RISK indicating higher chances of contracting lung cancer.\n Thus you are advised to receive immediate medical interventions\n to reduce risk factors.");
                }
                else{
                       result.setText("NO RISK"); 
                              result.setBackground(Color.green);
                              reco.setText("You have indicated NO RISK indicating no chances of contracting lung cancer.\n However,a number of factors may increase your risk of lung cancer in\n future. Hence avoid risk factors that can be controlled such \nas quitting smoking.");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Results.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
